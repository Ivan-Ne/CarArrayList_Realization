// Class Car
public class Car {

    // Private fields brand and number
    private String brand;
    private int number;

    // Constructor
    public Car(String brand, int number) {
        this.brand = brand;
        this.number = number;
    }

    // Getter for brand
    public String getBrand() {
        return brand;
    }

    // Getter for number
    public int getNumber() {
        return number;
    }
}
